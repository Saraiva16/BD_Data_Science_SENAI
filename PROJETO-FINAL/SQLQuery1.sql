USE LojaSENAI
CREATE TABLE TAB_CLIENTE (
COD_CLIENTE INTEGER PRIMARY KEY,
NOME VARCHAR(30) NOT NULL,
ENDERECO VARCHAR(50) NOT NULL,
SEXO VARCHAR(15) NOT NULL,
TELEFONE VARCHAR(20) NOT NULL
);

CREATE TABLE TAB_PRODUTOS(
COD_PRODUTO INTEGER PRIMARY KEY,
PRODUTO VARCHAR(30) NOT NULL,
GRUPO_PRODUTO VARCHAR(15) NOT NULL,
DESCRICAO VARCHAR(20) NOT NULL,
VALOR MONEY NOT NULL
);

CREATE TABLE TAB_VENDAS(
COD_VENDA INTEGER PRIMARY KEY,
COD_CLIENTE INTEGER NOT NULL,
COD_PRODUTO INTEGER NOT NULL,
DATA_VENDA DATE NOT NULL,
DESCRICAO_VENDA VARCHAR(20),
QTDE_ITENS INTEGER NOT NULL,
CIDADE_VENDA VARCHAR(50) NOT NULL,
FOREIGN KEY (COD_CLIENTE) REFERENCES TAB_CLIENTE(COD_CLIENTE),
FOREIGN KEY (COD_PRODUTO) REFERENCES TAB_PRODUTOS(COD_PRODUTO)
);


